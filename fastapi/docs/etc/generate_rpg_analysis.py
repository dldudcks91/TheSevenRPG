def draw_page(c, game, page_num, total_pages):
    """한 게임의 분석 내용을 여러 페이지에 걸쳐 그린다. 사용한 페이지 수를 반환."""
    pages_used = 0

    def start_new_page():
        nonlocal y, pages_used
        if pages_used > 0:
            # 페이지 번호
            c.setFont("Malgun", 7)
            c.setFillColor(COLOR_LIGHT)
            c.drawCentredString(WIDTH / 2, MARGIN_BOTTOM - 5,
                                f"-- {game['title']} ({pages_used + 1}) --")
            c.setFillColor(COLOR_TITLE)
            c.rect(MARGIN_LEFT - 5, MARGIN_BOTTOM + 2, CONTENT_WIDTH + 10, 1.5, fill=1, stroke=0)
            c.showPage()
        pages_used += 1
        y = HEIGHT - MARGIN_TOP

        # 상단 바
        c.setFillColor(COLOR_TITLE)
        c.rect(MARGIN_LEFT - 5, y - 2, CONTENT_WIDTH + 10, 3, fill=1, stroke=0)

        if pages_used == 1:
            # 첫 페이지: 타이틀
            y -= 22
            c.setFont("MalgunBold", 16)
            c.setFillColor(COLOR_TITLE)
            c.drawString(MARGIN_LEFT, y, game["title"])
            y -= 16
            c.setFont("Malgun", 9)
            c.setFillColor(COLOR_LIGHT)
            c.drawString(MARGIN_LEFT, y, game["subtitle"])
            y -= 10
        else:
            # 이후 페이지: 간략 타이틀
            y -= 18
            c.setFont("MalgunBold", 11)
            c.setFillColor(COLOR_LIGHT)
            c.drawString(MARGIN_LEFT, y, f"{game['title']} (계속)")
            y -= 8
        return y

    y = 0
    y = start_new_page()

    for section in game["sections"]:
        # 섹션 전에 공간 체크
        if y < MARGIN_BOTTOM + 80:
            y = start_new_page()

        # 구분선
        y -= 6
        c.setStrokeColor(COLOR_DIVIDER)
        c.setLineWidth(0.5)
        c.line(MARGIN_LEFT, y, WIDTH - MARGIN_RIGHT, y)
        y -= 12

        # 섹션 제목
        c.setFont("MalgunBold", 9.5)
        c.setFillColor(COLOR_ACCENT)
        c.drawString(MARGIN_LEFT, y, section["heading"])
        y -= 4

        # 불릿
        c.setFont("Malgun", 7.2)
        c.setFillColor(COLOR_TEXT)
        for bullet in section["bullets"]:
            # 불릿 시작 전 공간 체크
            estimated_lines = max(1, len(bullet) // 55 + 1)
            if y < MARGIN_BOTTOM + estimated_lines * 11 + 10:
                # 페이지 하단 바
                c.setFont("Malgun", 7)
                c.setFillColor(COLOR_LIGHT)
                c.drawCentredString(WIDTH / 2, MARGIN_BOTTOM - 5,
                                    f"-- {game['title']} ({pages_used}) --")
                c.setFillColor(COLOR_TITLE)
                c.rect(MARGIN_LEFT - 5, MARGIN_BOTTOM + 2, CONTENT_WIDTH + 10, 1.5, fill=1, stroke=0)
                c.showPage()
                pages_used += 1
                y = HEIGHT - MARGIN_TOP
                c.setFillColor(COLOR_TITLE)
                c.rect(MARGIN_LEFT - 5, y - 2, CONTENT_WIDTH + 10, 3, fill=1, stroke=0)
                y -= 18
                c.setFont("MalgunBold", 11)
                c.setFillColor(COLOR_LIGHT)
                c.drawString(MARGIN_LEFT, y, f"{game['title']} (계속)")
                y -= 8

                # 섹션 제목 다시
                y -= 6
                c.setFont("MalgunBold", 9)
                c.setFillColor(COLOR_SUBSECTION)
                c.drawString(MARGIN_LEFT, y, f"  {section['heading']} (계속)")
                y -= 4

                c.setFont("Malgun", 7.2)
                c.setFillColor(COLOR_TEXT)

            y -= 11
            # 불릿 기호
            c.drawString(MARGIN_LEFT + 4, y, "\u2022")
            # 텍스트
            max_width = CONTENT_WIDTH - 14
            text_obj = c.beginText(MARGIN_LEFT + 12, y)
            text_obj.setFont("Malgun", 7.2)

            line = ""
            for char in bullet:
                test_line = line + char
                if c.stringWidth(test_line, "Malgun", 7.2) > max_width:
                    text_obj.textLine(line)
                    line = char
                    y -= 10
                else:
                    line = test_line
            if line:
                text_obj.textLine(line)
            c.drawText(text_obj)

    # 마지막 페이지 하단
    c.setFont("Malgun", 7)
    c.setFillColor(COLOR_LIGHT)
    c.drawCentredString(WIDTH / 2, MARGIN_BOTTOM - 5,
                        f"-- {game['title']} ({pages_used}) --")
    c.setFillColor(COLOR_TITLE)
    c.rect(MARGIN_LEFT - 5, MARGIN_BOTTOM + 2, CONTENT_WIDTH + 10, 1.5, fill=1, stroke=0)

    return pages_used


